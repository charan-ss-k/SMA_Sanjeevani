/**
 * Helper function to translate dynamic data
 * @param {string} value - The value to translate
 * @param {string} category - 'symptom', 'condition', or 'medicine'
 * @param {string} language - Target language
 * @returns {string} Translated value or original value if not found
 */
export const translateData = (value, category, language) => {
  if (!value) return value;

  const normalizedValue = value.toLowerCase().replace(/\s+/g, '_');
  const langKey = language.toLowerCase();

  let translationMap;
  switch (category) {
    case 'symptom':
      translationMap = symptomsTranslations;
      break;
    case 'condition':
      translationMap = conditionsTranslations;
      break;
    case 'medicine':
      translationMap = medicinesTranslations;
      break;
    default:
      return value;
  }

  if (translationMap[normalizedValue] && translationMap[normalizedValue][langKey]) {
    return translationMap[normalizedValue][langKey];
  }

  // Fallback to English if translation not found
  if (translationMap[normalizedValue] && translationMap[normalizedValue].english) {
    return translationMap[normalizedValue].english;
  }

  return value;
};

/**
 * Batch translate array of values
 * @param {array} values - Array of values to translate
 * @param {string} category - 'symptom', 'condition', or 'medicine'
 * @param {string} language - Target language
 * @returns {array} Array of translated values
 */
export const translateDataBatch = (values, category, language) => {
  if (!Array.isArray(values)) return values;
  return values.map(val => translateData(val, category, language));
};
